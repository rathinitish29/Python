# common methods to delete data from list
fruit = ['mango', 'banana','apple','orange']
print(fruit)
#pop() method
fruit.pop(1)
print(fruit)
#del[] operator
del fruit[1]
print(fruit)
#remove()method
fruit.remove('mango')
print(fruit)
#to add data = append,insert,extend
#to delete = pop,del,remove